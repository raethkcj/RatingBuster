local NormalManaRegenPerSpi = function()
	local level = UnitLevel("player")
	local _, int = UnitStat("player", 4)
	local _, spi = UnitStat("player", 5)
	return (0.001 / spi + BaseManaRegenPerSpi[level] * (int ^ 0.5)) * 5
